	string getEarliest(int hourHand, int minuteHand) {
        for (int h=0; h<13; ++h) {
            for (int m=0; m<60; m+=2) {
                int min_hand = m*6;
                int hour_hand = h*30 + m/2;
                for (int by=0; by<360; by+=30) {
                    if ((hourHand+by)%360==hour_hand && (minuteHand+by)%360==min_hand) {
                        char buff[32];
                        sprintf(buff, "%02d:%02d", h, m);
                        return string(buff);
                    }
                }
            }
        }
        return string();
	}







   
